# Adding two number and printng the result
def add_numbers(a, b):
    return a + b

result  = add_numbers(5, 7)
print("The sum is:", result)

# The producton of the two numbers
def multiply_numbers(a, b): 
    return a * b
result = multiply_numbers(5, 7)
print("The product is:", result)

# The average of the two numbers
def average_numbers(a, b):
    return (a + b) / 2
result = average_numbers(5, 7)
print("The average is:", result)
